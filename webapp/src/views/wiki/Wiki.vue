                <h1 id="rust-">Rust 社区</h1>

                <p>Rust 编程语言有很多特质，但 Rust 最伟大的力量是，我们的社区团结一心，为更好的 Rust 体验在努力。</p>

                <p>我们致力于提供友好、安全和欢迎所有人的环境，无论性别，性取向，残疾，种族，宗教或类似的个人特征。我们的 <a href="https://www.rust-lang.org/conduct.html">行为守则</a> 为所有官方 Rust 论坛中的行为设定了标准。</p>

                <p>如果您感觉到不适或者正被社区成员骚扰，请立即 <a href="&#109;&#097;&#105;&#108;&#116;&#111;:&#114;&#117;&#115;&#116;&#045;&#109;&#111;&#100;&#115;&#064;&#114;&#117;&#115;&#116;&#045;&#108;&#097;&#110;&#103;&#046;&#111;&#114;&#103;">联系</a> <a href="team.html#Moderation-team">Rust 管理团队</a> 中的任何人。无论您是常规贡献者还是新来者，我们都十分关切您在社区的感受。</p>

                <h2 id="section">入门</h2>

                <p>对 Rust 来说，最重要的社区资源是：</p>

                <ul>
                  <li><a href="https://client00.chat.mibbit.com/?server=irc.mozilla.org&amp;channel=%23rust-beginners">#rust-beginners</a>，欢迎任何程度提问的 IRC 频道。</li>
                  <li><a href="https://users.rust-lang.org/">用户论坛</a>，讨论一切有关 Rust 的内容。</li>
                </ul>

                <p>您也可以在问答网站上得到帮助，例如 <a href="https://stackoverflow.com/questions/tagged/rust">Stack Overflow</a>。</p>

                <h2 id="section-1">新闻</h2>

                <p><a href="https://this-week-in-rust.org/">本周 Rust</a> 收集最新的消息、即将举办的活动，以及每周一次的有关 Rust 语言及库的变动记事。<a href="http://blog.rust-lang.org/">Rust 博客</a> 是 Rust 团队公告主要发展的地方。
                最后，几乎所有发生在 Rust 的变化都在非官方的 subreddit、<a href="https://www.reddit.com/r/rust">/r/rust</a> 上讨论。</p>

                <p>您还可以关注我们的 <a href="https://twitter.com/rustlang">Twitter</a> 账号或 <a href="http://weibo.com/u/5616913483">新浪微博</a>。</p>

                <h2 id="irc-">IRC 频道</h2>

                <p>Rustaceans 在 Mozilla 的 IRC 网络 irc.mozilla.org 上维护了一些友好、高流量的 <a href="https://en.wikipedia.org/wiki/Internet_Relay_Chat">IRC</a> 频道。</p>

                <p><a href="https://client00.chat.mibbit.com/?server=irc.mozilla.org&amp;channel=%23rust">#rust</a> 频道是讨论 Rust 的一般场所，并且是寻求帮助的好地方。你能找到愿意回答 Rust 有关的任何问题的人，响应通常很快。</p>

                <p>Rust 开发者们在 <a href="https://client00.chat.mibbit.com/?server=irc.mozilla.org&amp;channel=%23rust-internals">#rust-internals</a> 上协作。此频道中的实时讨论有关调整 Rust 本身，也包括向 Rust 贡献代码的问题。</p>

