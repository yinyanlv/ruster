                联系 <a href="team.html#Community-team">社区团队</a> 添加你的内容。</p>

                <h2 id="rust--1">Rust 团队</h2>

                <p>Rust 有一个社区驱动的开发过程，大多数决策都是通过公开的讨论和共识，在各方面 <a href="team.html">团队</a> 的指导下做出：</p>

                <ul>
                  <li>
                    <p><a href="team.html#Core-team">核心团队</a> 是负责指导设计和发展流程，监督新特性的引入，以及最终做出无法达成共识的决定（这很少发生）。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Language-design-team">语言设计团队</a> 负责设计新的语言特性。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Library-team">库团队</a> 负责 Rust 标准库、rust-lang 包，及公约。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Compiler-team">编译器团队</a> 负责编译器内部和优化。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Dev-tools-team">开发工具团队</a> 负责设定 Rust 开发工具的总体方向。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Cargo-team">Cargo 团队</a> 负责 Cargo 的设计和实现。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Infrastructure-team">基础设施团队</a> 负责支持项目本身的工具：CI、发布生成、机器人、指标。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Community-team">社区团队</a> 负责协调活动、推广、商业用户、教材和曝光。他们也可以就联络 Rust 当事人进行协调。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Moderation-team">管理团队</a> 负责维护 <a href="https://www.rust-lang.org/conduct.html">行为守则</a>。</p>
                  </li>
                  <li>
                    <p><a href="team.html#Documentation-team">文档团队</a> 负责确保 Rust 有出色的文档。</p>
                  </li>
                </ul>

                <p>除了上述官方团队名册，大多数团队也都有更大的一套了解该领域知识并且可以签发代码的审稿人。
                如果您有兴趣参与上述团队，随时与团队领导或其中的其他成员联系，他可以帮助你入门。</p>

                <h2 id="rust--2">Rust 开发</h2>

                <p>Rust 有超过 <a href="https://thanks.rust-lang.org/rust/all-time">2,000 名独立贡献者</a>，这个数字每周还都在增长。<a href="contribute.html">我们欢迎你加入到此行列中</a>！</p>

                <p>如上所述，<a href="https://internals.rust-lang.org/">Rust 内部论坛</a> 致力于讨论 Rust 的设计和实现。还有很多讨论发生在 GitHub 上：</p>

                <ul>
                  <li>
                    <p><a href="https://github.com/rust-lang/rust">主代码库</a> 和 <a href="https://github.com/rust-lang/rust/issues">问题跟踪器</a> 是最前沿的工作阵地。我们的审阅者力求友善引导并帮助新手，所以无需犹豫，打开拉取请求！</p>
                  </li>
                  <li>
                    <p><a href="https://github.com/rust-lang/rfcs">RFC 代码库</a> 跟踪我们的请求评论流程，Rust 社区和团队主要通过它就为语言、官方库和工具提出的新功能特性达成共识。</p>
                  </li>
                </ul>

